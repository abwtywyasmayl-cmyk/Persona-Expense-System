<?php
// بدء الجلسة (Session) حتى نتمكن من التحكم فيها
session_start();

// إعادة تهيئة جميع متغيرات الجلسة (تفريغها)
$_SESSION = array();

// تدمير الجلسة بالكامل (إلغاء تسجيل الدخول للمستخدم)
session_destroy();

// إعادة توجيه المستخدم إلى صفحة تسجيل الدخول بعد تسجيل الخروج
header("location: login.php");

// إيقاف تنفيذ أي أوامر أخرى بعد التوجيه
exit;
?>
